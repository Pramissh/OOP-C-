// MODULUS AND DIVISION OPERATORS
#include<iostream>
using namespace std;
int main(){
    int i=10, j=5;
    int modResult ;
    int divResult ;
    modResult = i%j;
    cout<<"Modulus division="<<modResult <<"\n";
    divResult = i/j;
    cout<<"Division="<<divResult<< "\n";
    return 0;
}